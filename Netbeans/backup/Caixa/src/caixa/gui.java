    private void enviardadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviardadosActionPerformed
        Properties props = new Properties();
            /** Parâmetros de conexão com servidor Gmail */
            props.put("mail.smtp.host", "smtp.gmail.com");
            props.put("mail.smtp.socketFactory.port", "465");
            props.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
            props.put("mail.smtp.auth", "true");
            props.put("mail.smtp.port", "465");

            Session session = Session.getDefaultInstance(props,
                        new javax.mail.Authenticator() {
                             protected PasswordAuthentication getPasswordAuthentication() 
                             {
                                   return new PasswordAuthentication("pedroramosclash@gmail.com", "filhopedro");
                             }
                        });
            /** Ativa Debug para sessão */
            session.setDebug(true);
            try {

                  Message message = new MimeMessage(session);
                  message.setFrom(new InternetAddress("pedroramosclash@gmail.com")); //Remetente

                  Address[] toUser = InternetAddress //Destinatário(s)
                             .parse("evconta1@gmail.com");  
                  message.setRecipients(Message.RecipientType.TO, toUser);
                  message.setSubject("Finalização Caixa SHOPPING DA MODA INTIMA");//Assunto
                  message.setText("Total vendas = "+totalvendas.getText()+"\nCaixa dia Anterior = "+caixadiaanterior.getText()+
								  "\nCaixa Atual = "+caixaatual.getText()+"\nTotal Dinheiro = "+totaldinheiro.getText()+""
								 +"\nTotal Cartão = "+totalcartao.getText()+"\nRetirada = "+retirada.getText()+"\nComentario sobre retirada: "
								+comentario.getText()+"\nAcrescimo = "
								 +acrescimo.getText()+"\nCaixa = "+caixa.getText());
                  /**Método para enviar a mensagem criada*/
                  Transport.send(message);
                  System.out.println("Feito!!!");
             } catch (MessagingException e) {
                  throw new RuntimeException(e);
            }
    }//GEN-LAST:event_enviardadosActionPerformed

