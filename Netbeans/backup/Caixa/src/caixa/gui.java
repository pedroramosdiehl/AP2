    private void calculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculaActionPerformed
		
		double totalV = Double.parseDouble(totalvendas.getText());
		double caixaDA = Double.parseDouble(caixadiaanterior.getText());
		double caixaA = Double.parseDouble(caixaatual.getText());
		double totalD = Double.parseDouble(totaldinheiro.getText()); 
		double totalC = Double.parseDouble(totalcartao.getText());
		double ret = Double.parseDouble(retirada.getText());
		double acre = Double.parseDouble(acrescimo.getText());
		double pedidoDV = Double.parseDouble(ped.getText());
		//865 - 817,59 + 5 - 50
		double soma = (totalV + caixaDA) - (caixaA + totalD + totalC) + (acre + pedidoDV) - ret ;
		
		caixa.setText(""+soma);
    }//GEN-LAST:event_calculaActionPerformed

